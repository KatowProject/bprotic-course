/** Operator Aritmatika */
// Penjumlahan (+)
console.log(10 + 10); //20

// Pengurangan (-)
console.log(30 - 4); //26

// Perkalian ( * )
console.log(7 * 3); //21

// Modulus/sisa bagi ( % );
console.log(19 % 3) //1

// Increment/tambah 1 ( ++ )
console.log(5++) //6 
// 5 + 1 = 6

//Decrement/kurang 1 ( -- )
console.log(9--) //8

//Exponent/Pemangkatan ( ** )
console.log(9 * 3) //729
// 9 * 9 * 9 = 729


